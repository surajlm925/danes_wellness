// Fallback: try to match by handle keywords
export function getImageByHandle(handle) {
    // Direct match first
    if (imageMap[handle]) return imageMap[handle];

    // Fuzzy match on partial handle
    const keys = Object.keys(imageMap);
    const fuzzy = keys.find(k => handle.includes(k) || k.includes(handle.split('-')[0]));
    if (fuzzy) return imageMap[fuzzy];

    return null; // will show placeholder
}